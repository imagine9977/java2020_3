package sec1;

public class GOFExam {
	public static void main(String[] args) {
		//GOF패턴:생성, 구조 ,행위
		//생성(Creational) Pattern: 개체생성을 자바 디자인 패턴, 객체 (Instance) 생성에 관련된 패턴으로 객체가 생성되거나 변경되어도 
		//구조(Structural) Pattern: 여러 클래스를 혼합하기 위한 자바 디자인 패턴, 객체와 클래스를 보다 큰 구조로 조립하여 유연하고, 효율적인 방법을 제공하는 패턴 
		//행위(Behavior) Pattern: 메소드와 관련하여 기능을 분해하고너 조합하기 위한 자바 디자인 패턴 
		//공유하는 자원은 객체가 하나 여야 한다 => 따라서 SIngleton을 많이 사용
		System.out.println("GoF(Gang of Fout) 패턴: SOLID의 5가지 객체 지향 프로그래밍 설계원칙이 ");
	}
}
